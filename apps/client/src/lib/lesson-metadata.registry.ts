// Helper function to get metadata by slug
export function getLessonMetadata(
  courseType: string,
  slug: string,
): LessonMetadata | null {
  if (courseType === 'sql') {
    return sqlLessonsMetadata[slug] || null
  }
  if (courseType === 'mongodb') {
    return mongodbLessonsMetadata[slug] || null
  }
  return null
}

// Helper function to get all metadata for a course
export function getAllLessonMetadata(courseType: string): LessonMetadata[] {
  if (courseType === 'sql') {
    return Object.values(sqlLessonsMetadata)
  }
  if (courseType === 'mongodb') {
    return Object.values(mongodbLessonsMetadata)
  }
  return []
}

