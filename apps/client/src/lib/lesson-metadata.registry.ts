/**
 * Generate lesson metadata
 */
function createLessonMetadata(
  data: Omit<
    LessonMetadata,
    | 'author'
    | 'publisher'
    | 'ogImage'
    | 'twitterCard'
    | 'inLanguage'
    | 'isAccessibleForFree'
    | 'canonicalUrl'
    | 'breadcrumbs'
  >,
): LessonMetadata {
  return {
    ...data,
    author: DEFAULT_AUTHOR,
    publisher: DEFAULT_PUBLISHER,
    ogImage: SITE_LOGO,
    twitterCard: 'summary_large_image',
    inLanguage: 'en-US',
    isAccessibleForFree: true,
    canonicalUrl: `${SITE_URL}/learning/${data.courseType}/${data.slug}`,
    breadcrumbs: [
      { position: 1, name: 'Home', item: SITE_URL },
      {
        position: 2,
        name: data.courseType.toUpperCase(),
        item: `${SITE_URL}/learning/${data.courseType}`,
      },
      {
        position: 3,
        name: data.moduleTitle,
        item: `${SITE_URL}/learning/${data.courseType}/${data.slug}`,
      },
      {
        position: 4,
        name: data.title,
        item: `${SITE_URL}/learning/${data.courseType}/${data.slug}`,
      },
    ],
  }
}

